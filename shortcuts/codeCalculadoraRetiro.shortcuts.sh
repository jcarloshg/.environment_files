function codeCalculadoraRetiro {

    route="$HOME/Documentos/kokonut/Coppel_Calculadora_Web/Coppel_Calculadora_web_Front"

    echo "${JUMPLINE} > executing...    ${MAGENTA}[     ${RED}cd ${MAGENTA}${route}     ]"
    NCOLOR
    cd "${route}"

    echo "${JUMPLINE} > executing...    ${MAGENTA}[     ${RED}code ${MAGENTA}${route}   ]"
    NCOLOR
    code "${route}"
}
